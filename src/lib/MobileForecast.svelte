    function pickcolor(knots: number, style: boolean = true): string | number[] {
        knots = Math.round(knots)
        var maxknots = 50
        var r: number, g: number, b: number = 0
        var perc = knots * 100 / maxknots

        if (perc < 20) {
            r = 0
            g = 255 / 20 * perc
            b = 255
        } else if (perc < 40) {
            r = 0
            g = 255
            b = 255 - 255 / 40 * perc
        } else if (perc < 60) {
            r = 255 / 60 * perc
            g = 255
            b = 0
        } else if (perc < 80) {
            r = 255
